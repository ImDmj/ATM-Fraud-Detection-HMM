package hmm;

public class HistorySeq {

    private String sequenceOp;
    private String type;

    /**
     * @return the sequenceOp
     */
    public String getSequenceOp() {
        return sequenceOp;
    }

    /**
     * @param sequenceOp the sequenceOp to set
     */
    public void setSequenceOp(String sequenceOp) {
        this.sequenceOp = sequenceOp;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
}
