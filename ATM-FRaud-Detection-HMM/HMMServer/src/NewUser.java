    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            UserAuthetication user = new UserAuthetication();
            user.Con();
            String fullname = txtfullname.getText().trim();
            String address = txtaddress.getText().trim();
            String phno = txtcontactno.getText().trim();
            String emailid = txtemailid.getText().trim();
            String city = txtcity.getText().trim();
            String ttype = txtacctype.getSelectedItem().toString();
            String accno = txtaccount.getText().trim();
            String bal = txtamount.getText().trim();
            String rfid = txtcardno.getText().trim();
            FileInputStream stream = null;
            stream = new FileInputStream(new File("C:\\Output\\" + txtcardno.getText() + ".fpt"));
            byte[] data = null;
            try {
                data = new byte[stream.available()];
            } catch (IOException ex) {
                System.out.println("Error :" + ex.toString());
            }
            try {
                stream.read(data);
            } catch (IOException ex) {
            }
            try {
                stream.close();
            } catch (IOException ex) {
            }
            if (user.AddCustomer(fullname, address, phno, city, emailid, accno, data)) {
                user.CloseConnection();
                user.Con();
                if (user.CreateAccount(accno, ttype, bal, rfid, phno)) {
                    JOptionPane.showMessageDialog(this, "New User Account Created Successfully !");
                    this.dispose();
                }
            }
        } catch (Exception err) {
            System.out.println("Error " + err.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

